import { createClient } from '@supabase/supabase-js'
import { readFileSync } from 'fs'
import { resolve } from 'path'

// Read .env.local
const envPath = resolve(process.cwd(), '.env.local')
const envContent = readFileSync(envPath, 'utf-8')
const env = Object.fromEntries(
  envContent.split('\n').filter(l => l && !l.startsWith('#')).map(l => {
    const idx = l.indexOf('=')
    return [l.slice(0, idx), l.slice(idx + 1)]
  })
)

const supabaseUrl = env.NEXT_PUBLIC_SUPABASE_URL
const serviceKey = env.SUPABASE_SERVICE_ROLE_KEY

if (!supabaseUrl || !serviceKey) {
  console.error('Missing SUPABASE_URL or SERVICE_ROLE_KEY in .env.local')
  process.exit(1)
}

const supabase = createClient(supabaseUrl, serviceKey, {
  auth: { autoRefreshToken: false, persistSession: false }
})

// SEO settings to seed — these resolve audit recommendations #1–#5, #7, #8, #9
const seoSettings = {
  // #1 CRITICAL: Site title
  seo_siteTitle: 'Go Virall — Social Intelligence Platform',

  // #2 CRITICAL: Site description (155 chars — within 120-160 optimal)
  seo_siteDescription:
    'Go Virall is the AI-powered social intelligence platform that helps creators and influencers grow their audience, analyze performance, and land brand deals.',

  // #3 WARNING: Default OG image (dynamic image generated by opengraph-image.tsx)
  seo_defaultOgImage: 'https://www.govirall.com/opengraph-image',

  // #4 WARNING: Canonical URL base
  seo_canonicalUrlBase: 'https://www.govirall.com',

  // #5 WARNING: Google Analytics (already tracking on site via layout.tsx)
  seo_googleAnalyticsId: 'G-P1HL4ZZ8QK',

  // Twitter card type
  seo_twitterCardType: 'summary_large_image',

  // #8 INFO: Organization schema
  seo_organizationName: 'Go Virall',
  seo_organizationDescription:
    'Go Virall is a social intelligence platform that provides AI-powered analytics, audience insights, trust scoring, and brand deal management for creators and influencers.',
  seo_organizationContactInfo: 'support@govirall.com',
  seo_organizationLogo: 'https://www.govirall.com/icon.png',
}

async function seedSettings() {
  console.log('Seeding SEO settings into admin_settings...\n')

  let saved = 0
  let skipped = 0

  for (const [key, value] of Object.entries(seoSettings)) {
    // Check if setting already exists and has a non-empty value
    const { data: existing } = await supabase
      .from('admin_settings')
      .select('key, value')
      .eq('key', key)
      .single()

    if (existing?.value?.trim()) {
      console.log(`  SKIP  ${key} (already set: "${existing.value.substring(0, 50)}...")`)
      skipped++
      continue
    }

    // Delete any existing empty row
    await supabase.from('admin_settings').delete().eq('key', key)

    // Insert the setting
    const { error } = await supabase.from('admin_settings').insert({
      key,
      value,
      updated_at: new Date().toISOString(),
    })

    if (error) {
      console.error(`  FAIL  ${key}: ${error.message}`)
    } else {
      console.log(`  OK    ${key} = "${value.substring(0, 60)}${value.length > 60 ? '...' : ''}"`)
      saved++
    }
  }

  console.log(`\nDone: ${saved} saved, ${skipped} skipped (already set)`)
  console.log('\nRemaining manual tasks:')
  console.log('  - #7 Google Search Console: Add GSC verification meta tag or connect via OAuth')
  console.log('  - #9 Social profiles: Fill in your social media URLs in SEO Settings')
  console.log('  - #6 Blog cover images: Add cover images to published blog posts')
}

seedSettings().catch(console.error)
